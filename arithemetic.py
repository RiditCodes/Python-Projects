def addition(a, b):
    print(f"{a} + {b} =", a+b)

def subtraction(a, b):
    print(f"{a} - {b} =", a-b)

def division(a, b):
    print(f"{a} / {b} =", a/b)

def multiplication(a, b):
    print(f"{a} * {b} =", a*b)

def power(a, b):
    print(f"{a} ** {b} =", a**b)

def floor(a, b):
    print(f"{a} // {b} =", a//b)

def modulo(a, b):
    print(f"{a} % {b} =", a%b)