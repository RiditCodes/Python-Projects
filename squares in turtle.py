import turtle

start = turtle.Turtle()
def make_two_square():
    start.forward(150)
    start.left(90)
    start.forward(150)
    start.left(90)
    start.forward(150)
    start.left(90)
    start.forward(300)
    start.right(90)
    start.forward(150)
    start.right(90)
    start.forward(150)
    start.right(90)
    start.forward(150)
make_two_square()
