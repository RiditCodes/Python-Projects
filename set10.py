A = {1, 2, 3, 4, 5}
B = {3, 5, 1}

#It will display True as all the elements of B exist in A.
print(A>B)

#It will display False as all the elements of A do not exist in B.
print(A<B)

'''
Set A is called superset of set B and set B is called subset of 
set A as all the elements of set B are present in set A.
'''